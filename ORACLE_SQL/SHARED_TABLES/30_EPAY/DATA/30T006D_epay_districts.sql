DROP TABLE EPAY.EPAY_DISTRICTS CASCADE CONSTRAINTS PURGE;

CREATE TABLE EPAY.EPAY_DISTRICTS (
   DISTRICT_ID       NUMBER NOT NULL,
   REGION_ID         NUMBER,
   DISTRICT_NAME     VARCHAR2 (200),
   DISTRICT_SYMBOL   VARCHAR2 (10),
   CONSTRAINT PK_DISTRICT_ID PRIMARY KEY (DISTRICT_ID)
)
TABLESPACE RHODB
PCTUSED 0
PCTFREE 10
INITRANS 1
MAXTRANS 255
STORAGE (PCTINCREASE 0 BUFFER_POOL DEFAULT)
LOGGING
NOCOMPRESS
NOCACHE
NOPARALLEL
MONITORING;

DROP SEQUENCE EPAY.EPAY_DISTRICTS_SEQ;

CREATE SEQUENCE EPAY.EPAY_DISTRICTS_SEQ
   START WITH 1
   MAXVALUE 9223372036854775807
   MINVALUE 1
   NOCYCLE
   NOCACHE
   ORDER;

CREATE OR REPLACE TRIGGER EPAY.EPAY_DISTRICTS_TRG
   BEFORE INSERT
   ON EPAY.EPAY_DISTRICTS
   FOR EACH ROW
   -- OPTIONALLY RESTRICT THIS TRIGGER TO FIRE ONLY WHEN REALLY NEEDED
   WHEN (NEW.DISTRICT_ID IS NULL)
DECLARE
   V_ID   EPAY.EPAY_DISTRICTS.DISTRICT_ID%TYPE;
BEGIN
   SELECT   EPAY.EPAY_DISTRICTS_SEQ.NEXTVAL INTO V_ID FROM DUAL;

   :NEW.DISTRICT_ID := V_ID;
END EPAY_DISTRICTS_TRG;