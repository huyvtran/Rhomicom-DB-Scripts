DROP TABLE EPAY.EPAY_REGIONS CASCADE CONSTRAINTS PURGE;

CREATE TABLE EPAY.EPAY_REGIONS (
   REGION_ID    NUMBER NOT NULL,
   REGION       VARCHAR2 (50),
   SYMBOL       VARCHAR2 (10),
   DAS_SYMBOL   VARCHAR2 (10),                -- DIGITAL ADDRESS SYSTEM SYMBOL
   CONSTRAINT PK_REGION_ID PRIMARY KEY (REGION_ID)
)
TABLESPACE RHODB
PCTUSED 0
PCTFREE 10
INITRANS 1
MAXTRANS 255
STORAGE (PCTINCREASE 0 BUFFER_POOL DEFAULT)
LOGGING
NOCOMPRESS
NOCACHE
NOPARALLEL
MONITORING;

COMMENT ON COLUMN EPAY.EPAY_REGIONS.DAS_SYMBOL IS
'digital address system symbol';

DROP SEQUENCE EPAY.EPAY_REGIONS_SEQ;

CREATE SEQUENCE EPAY.EPAY_REGIONS_SEQ
   START WITH 1
   MAXVALUE 9223372036854775807
   MINVALUE 1
   NOCYCLE
   NOCACHE
   ORDER;

CREATE OR REPLACE TRIGGER EPAY.EPAY_REGIONS_TRG
   BEFORE INSERT
   ON EPAY.EPAY_REGIONS
   FOR EACH ROW
   -- OPTIONALLY RESTRICT THIS TRIGGER TO FIRE ONLY WHEN REALLY NEEDED
   WHEN (NEW.REGION_ID IS NULL)
DECLARE
   V_ID   EPAY.EPAY_REGIONS.REGION_ID%TYPE;
BEGIN
   SELECT   EPAY.EPAY_REGIONS_SEQ.NEXTVAL INTO V_ID FROM DUAL;

   :NEW.REGION_ID := V_ID;
END EPAY_REGIONS_TRG;