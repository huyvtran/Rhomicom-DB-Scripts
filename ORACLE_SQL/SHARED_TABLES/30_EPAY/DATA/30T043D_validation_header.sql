DROP TABLE EPAY.VALIDATION_HEADER CASCADE CONSTRAINTS PURGE;

CREATE TABLE EPAY.VALIDATION_HEADER (
   VLDTN_HDR_ID       NUMBER NOT NULL,
   DESCRIPTION        CLOB,
   DATA_TYPE          VARCHAR2 (20),
   STATUS             VARCHAR2 (10),
   PSB_X_ID           NUMBER,
   PSB_Y_ID           NUMBER,
   CREATED_BY         NUMBER,
   CREATION_DATE      VARCHAR2 (21),
   LAST_UPDATE_BY     NUMBER,
   LAST_UPDATE_DATE   VARCHAR2 (21),
   CONSTRAINT PK_VLDTN_ID PRIMARY KEY (VLDTN_HDR_ID)
)
TABLESPACE RHODB
PCTUSED 0
PCTFREE 10
INITRANS 1
MAXTRANS 255
STORAGE (PCTINCREASE 0 BUFFER_POOL DEFAULT)
LOGGING
NOCOMPRESS
NOCACHE
NOPARALLEL
MONITORING;

DROP SEQUENCE EPAY.VALIDATION_HEADER_SEQ;

CREATE SEQUENCE EPAY.VALIDATION_HEADER_SEQ
   START WITH 1
   MAXVALUE 9223372036854775807
   MINVALUE 1
   NOCYCLE
   NOCACHE
   ORDER;

CREATE OR REPLACE TRIGGER EPAY.VALIDATION_HEADER_TRG
   BEFORE INSERT
   ON EPAY.VALIDATION_HEADER
   FOR EACH ROW
   -- OPTIONALLY RESTRICT THIS TRIGGER TO FIRE ONLY WHEN REALLY NEEDED
   WHEN (NEW.VLDTN_HDR_ID IS NULL)
DECLARE
   V_ID   EPAY.VALIDATION_HEADER.VLDTN_HDR_ID%TYPE;
BEGIN
   SELECT   EPAY.VALIDATION_HEADER_SEQ.NEXTVAL INTO V_ID FROM DUAL;

   :NEW.VLDTN_HDR_ID := V_ID;
END VALIDATION_HEADER_TRG;