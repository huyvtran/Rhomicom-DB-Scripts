CREATE OR REPLACE FUNCTION ORG.GET_ORG_NAME (P_ORGID NUMBER)
   RETURN VARCHAR2
AS
   L_RESULT   VARCHAR2 (200 BYTE);
BEGIN
   SELECT   ORG_NAME
     INTO   L_RESULT
     FROM   ORG.ORG_DETAILS
    WHERE   ORG_ID = P_ORGID;

   RETURN L_RESULT;
EXCEPTION
   WHEN OTHERS
   THEN
      RETURN '';
END;
