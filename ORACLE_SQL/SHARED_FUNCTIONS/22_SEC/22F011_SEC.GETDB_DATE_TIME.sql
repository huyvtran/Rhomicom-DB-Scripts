CREATE OR REPLACE FUNCTION SEC.GETDB_DATE_TIME
   RETURN VARCHAR2
AS
   L_RESULT   VARCHAR2 (200 BYTE);
BEGIN
   SELECT   TO_CHAR (SYSDATE, 'RRRR-MM-DD HH24:MI:SS') INTO L_RESULT FROM DUAL;

   RETURN L_RESULT;
EXCEPTION
   WHEN OTHERS
   THEN
      RETURN '';
END;
